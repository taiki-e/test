set -CeEuo pipefail
IFS=$'\n\t'
trap -- 's=$?; printf >&2 "%s\n" "${0##*/}:${LINENO}: \`${BASH_COMMAND}\` exit with ${s}"; exit ${s}' ERR
cd -- "$(dirname -- "$0")"/..

tag="${1:-v0.0.0}"
gh release delete "${tag}" -y || true
git tag -f "${tag}"
git push origin -f refs/tags/"${tag}"
